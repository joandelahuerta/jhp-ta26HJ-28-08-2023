package M6;

public class Rueda {
    private String marca;
    private double diametro;

    public Rueda(String marca, double diametro) {
        this.marca = marca;
        this.diametro = diametro;
    }

    public String getMarca() {
        return marca;
    }

    public double getDiametro() {
        return diametro;
    }
}